package Basics;

public class Variables {
    public static void main(String[] args) {
//        case sensitive
        int _age = 21;
        int age = 22;
        int $age = 23;

        System.out.println(_age);
        System.out.println(age);
        System.out.println($age);

//        variable name need to make sense
//        use camel case
        int myAge = 24;
        System.out.println(myAge);
    }

}
